#企业发放的奖金根据利润提成。利润(I)低于或等于10万元时，奖金可提10%；
# 利润高于10万元，低于20万元时，低于10万元的部分按10%提成，高于10万元的部分，可提成7.5%；
# 20万到40万之间时，高于20万元的部分，可提成5%；
# 40万到60万之间时高于40万元的部分，可提成3%；
# 60万到100万之间时，高于60万元的部分，可提成1.5%，
# 高于100万元时，超过100万元的部分按1%提成，从键盘输入当月利润I，求应发放奖金总数？

I = int(input('请输入利润值：'))
if I > 100000 and I < 200000:
    a = (I - 100000) * 0.075 + 100000 * 0.1
elif I > 200000 and I < 400000:
    a = (I - 200000) * 0.05 + (200000 - 100000) * 0.075 + 100000 * 0.1
elif I > 400000 and I < 600000:
    a = (I - 400000) * 0.03 + (400000 - 200000) * 0.005 + (200000 - 100000) * 0.075 + 100000 * 0.1
elif I > 600000 and I < 1000000:
    a = (I - 600000) * 0.015 + (600000 - 400000) * 0.003 + (400000 - 200000) * 0.005 + (200000 - 100000) * 0.075 + 100000 * 0.1
elif I > 1000000:
    a = (I - 1000000) * 0.01 + (1000000 - 600000) * 0.0015 + (600000 - 400000) * 0.003 + (400000 - 200000) * 0.005 + (200000 - 100000) * 0.075 + 100000 * 0.1
else:
    a = I * 0.1
print('本月奖金为：',a)